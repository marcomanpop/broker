        document.addEventListener("DOMContentLoaded", handleURLMessage);

        // Handle message from URL and display it using SweetAlert
        function handleURLMessage() {
            const urlParams = new URLSearchParams(window.location.search);
            const message = urlParams.get('message');
            const messageType = urlParams.get('message_type');

            if (message) {
                const iconType = messageType === "success" ? "success" : "error"; // Define icon based on type

                Swal.fire({
                    title: iconType === "success" ? "Success!" : "Error!",
                    text: decodeURIComponent(message),
                    icon: iconType,
                    confirmButtonText: "OK"
                });

                // Remove message from URL after displaying
                history.replaceState(null, '', window.location.pathname);
            }
        }

        document.getElementById("signupForm")?.addEventListener("submit", async function(event) {
            event.preventDefault(); // Prevent traditional form submission

            // Get form values
            const fullname = document.getElementById("fullname")?.value.trim();
            const username = document.getElementById("username")?.value.trim();
            const home_address = document.getElementById("home_address")?.value.trim();
            const city = document.getElementById("city")?.value.trim();
            const postal_code = document.getElementById("postal_code")?.value.trim();
            const country = document.getElementById("country")?.value.trim();
            const currency = document.getElementById("currency")?.value.trim() || "USD";
            const phone = document.getElementById("phone")?.value.trim();
            const email = document.getElementById("email")?.value.trim();
            const password = document.getElementById("password")?.value.trim();
            const password_confirmation = document.getElementById("confirm_password")?.value.trim();
            const checkbox = document.querySelector(".uk-checkbox");
            const testQuestionAnswer = document.getElementById("testQuestionAnswer")?.value.trim().toLowerCase();

            // Check for empty fields
            if (!fullname || !username || !phone || !email || !password) {
                Swal.fire({
                    title: "Error",
                    text: "All required fields (Full Name, Username, Phone, Email, Password) must be filled!",
                    icon: "error",
                    confirmButtonText: "OK"
                });
                return;
            }

            if (password !== password_confirmation) {
                Swal.fire({
                    title: "Error",
                    text: "Passwords don't match",
                    icon: "error",
                    confirmButtonText: "OK"
                });
                return;
            }

            // Check if checkbox is checked
            if (!checkbox?.checked) {
                Swal.fire({
                    title: "Error",
                    text: "You must agree to the terms and conditions!",
                    icon: "error",
                    confirmButtonText: "OK"
                });
                return;
            }

            // Validate test question answer
            if (testQuestionAnswer !== "white") {
                Swal.fire({
                    title: "Error",
                    text: "Incorrect answer to the test question. Please try again.",
                    icon: "error",
                    confirmButtonText: "OK"
                });
                return;
            }

            // Submit the form if all required fields are filled and test question is correct
            document.getElementById('signupForm')?.submit();
        });
